// Search Modal Functions
function setupSearchModal() {
    searchBtn.addEventListener('click', () => {
        searchModal.style.display = 'block';
        searchInput.focus();
    });

    closeModal.addEventListener('click', () => {
        searchModal.style.display = 'none';
    });

    window.addEventListener('click', (e) => {
        if (e.target === searchModal) {
            searchModal.style.display = 'none';
        }
    });

    searchSubmit.addEventListener('click', performSearch);
    searchInput.addEventListener('keypress', (e) => {
        if (e.key === 'Enter') {
            performSearch();
        }
    });
}

function performSearch() {
    const query = searchInput.value.trim().toLowerCase();
    if (!query) return;

    searchResults.innerHTML = '<div class="loading"><div class="loading-spinner"></div></div>';

    setTimeout(() => {
        const results = sampleProducts.filter(product =>
            product.name.toLowerCase().includes(query) ||
            product.category.toLowerCase().includes(query) ||
            product.description.toLowerCase().includes(query)
        );

        if (results.length === 0) {
            searchResults.innerHTML = `<p class="text-center">No products found for "${query}". Try a different search.</p>`;
            return;
        }

        searchResults.innerHTML = `
            <h3>Found ${results.length} results for "${query}"</h3>
            <div class="products-grid" style="grid-template-columns: repeat(auto-fill, minmax(250px, 1fr)); gap: 1.5rem;">
                ${results.map(product => `
                    <div class="product-card">
                        <div class="product-image">
                            <i class="fas ${product.image}"></i>
                        </div>
                        <div class="product-info">
                            <h4 class="product-title">${product.name}</h4>
                            <div class="product-price">$${product.price.toFixed(2)}</div>
                            <div class="product-rating">
                                ${'⭐'.repeat(Math.floor(product.rating))}${'☆'.repeat(5 - Math.floor(product.rating))}
                            </div>
                            <p class="product-description">${product.description.substring(0, 100)}...</p>
                            <button class="action-btn add-to-cart" style="width: 100%;" data-id="${product.id}">
                                <i class="fas fa-shopping-cart"></i> Add to Cart
                            </button>
                        </div>
                    </div>
                `).join('')}
            </div>
        `;

        // Add event listeners to search result buttons
        searchResults.querySelectorAll('.add-to-cart').forEach(btn => {
            btn.addEventListener('click', (e) => {
                const productId = parseInt(e.target.closest('[data-id]').dataset.id);
                addToCart(productId);
                searchModal.style.display = 'none';
            });
        });
    }, 500);
}

// Cart Functions
function setupCart() {
    updateCartDisplay();
}

function addToCart(productId) {
    const product = sampleProducts.find(p => p.id === productId);
    if (!product) return;

    const existingItem = cartItems.find(item => item.id === productId);
    if (existingItem) {
        existingItem.quantity += 1;
    } else {
        cartItems.push({ ...product, quantity: 1 });
    }

    updateCartDisplay();
    showNotification(`${product.name} added to cart!`);
}

function updateCartDisplay() {
    const cartCount = cartItems.reduce((total, item) => total + item.quantity, 0);
    cartBtn.innerHTML = `<i class="fas fa-shopping-cart"></i> ${translations[currentLang].cart} (${cartCount})`;

    // Save to localStorage
    localStorage.setItem('cartItems', JSON.stringify(cartItems));
}

function viewProductDetails(productId) {
    const product = sampleProducts.find(p => p.id === productId);
    if (!product) return;

    alert(`Product Details:\n\nName: ${product.name}\nPrice: $${product.price}\nCategory: ${product.category}\nRating: ${'⭐'.repeat(Math.floor(product.rating))}\n\nDescription:\n${product.description}`);
}

function showNotification(message) {
    // Create notification element
    const notification = document.createElement('div');
    notification.className = 'notification';
    if (document.body.classList.contains('day-mode')) {
        notification.classList.add('day-mode');
    }
    notification.innerHTML = `
        <i class="fas fa-check-circle"></i>
        <span>${message}</span>
    `;

    // Add to body
    document.body.appendChild(notification);

    // Show notification
    setTimeout(() => {
        notification.classList.add('show');
    }, 100);

    // Hide and remove after delay
    setTimeout(() => {
        notification.classList.remove('show');
        setTimeout(() => {
            notification.remove();
        }, 300);
    }, 3000);
}

// Animations Functions
function setupAnimations() {
    // Animate elements on scroll
    const observerOptions = {
        threshold: 0.1
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('animate');
            }
        });
    }, observerOptions);

    document.querySelectorAll('.animate-on-scroll').forEach(el => {
        observer.observe(el);
    });

    // Animate floating icons
    const floatingIcons = document.querySelector('.floating-icons');
    if (floatingIcons) {
        floatingIcons.addEventListener('mouseenter', () => {
            floatingIcons.style.animationPlayState = 'paused';
        });

        floatingIcons.addEventListener('mouseleave', () => {
            floatingIcons.style.animationPlayState = 'running';
        });
    }

    // Load cart from localStorage
    const savedCart = localStorage.getItem('cartItems');
    if (savedCart) {
        cartItems = JSON.parse(savedCart);
        updateCartDisplay();
    }
}

